

public class Math_Class {

    public static void main(String[] args) {

        System.out.println(Math.PI); //Pi Value
        System.out.println(Math.E); //Exponential Constant (Euler's number)

        double result;

        result = Math.pow(2, 4);   //Raise to power of
        result = Math.abs(23);     //Absolute Value
        result = Math.sqrt(9);     //Square root
        result = Math.round(6.14); //Round to nearest whole number
        result = Math.ceil(7.63);  //Round up
        result = Math.floor(4.78); //Round down
        result = Math.max(10, 6);  //Highest number between the two
        result = Math.min(24, 15); //Lowest number between the two

        System.out.println(result);
    }
}
